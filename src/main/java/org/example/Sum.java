package org.example;

public class Sum {

    // public = für alle einsehbar
    // static = Methode einer Klasse
    // (1.) int = Datentyp der zurückgegeben wird
    // sum = Name der Methode
    // (int a, int b) = Parameter der Methode = Daten die wir der Methode geben
    public static int sum(int a, int b) {
        int sumOfAPlusB = a + b;
        return sumOfAPlusB;
    }
}
